void	ft_memswap(void *a, void *b)
{
	char	*ac;
	char	*bc;

	ac = (char *)a;
	bc = (char *)b;
	*ac = *ac ^ *bc;
	*bc = *bc ^ *ac;
	*ac = *ac ^ *bc;
}
